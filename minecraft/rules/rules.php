	<a id="7-2-3" class="anchor"></a><a href="#7-2-3">3.</a>ルール違反による処置、プレイヤー間のトラブル、ゲーム内不具合、プレイヤーの過失によって発生した損失の補填はできませんのでご了承ください。<br>
	<a id="7-2-4" class="anchor"></a><a href="#7-2-4">4.</a>サーバーメンテナンス時に強制的に切断されたことによる損失などの補填はできませんのでご了承ください。<br>
	<a id="7-2-5" class="anchor"></a><a href="#7-2-5">5.</a>サーバーのロールバックが必要になる事象が発生した場合、ロールバックによって発生した損失などの補填はできませんのでご了承ください。<br>
