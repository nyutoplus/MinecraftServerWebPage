<h1 class="content-title">サーバールール</h1>
<p>
	サーバーに参加するプレイヤーの皆様(以下、プレイヤー)は当サーバーに参加した時点で本ルールに同意したものとします。<br> 本ルールはサーバー内の情勢、運営の都合で予告なく変更されることがあります。変更した際には<a class="hide-line" href="https://discord.gg/wshGjfg">Discordサーバー</a>にて通知しますので是非サーバーに参加してください。<br>
</p>
<hr>
<p>
<h2 class="content-subtitle">第1条 サーバーの禁止事項</h2>
<h3 style="font-size: 1.3rem; margin-left: 1rem">第1項 他人のプレイに支障をきたす行為</h3>
<ul style="margin-left: 1rem;">
	<li>・公共設備の破壊等

	<li>・他プレイヤー、企業、団体への誹謗中傷、暴言、脅迫等

	<li>・過剰な要求、命令、宣伝等

	<li>・他プレイヤーの迷惑となるチャット発言(過度な論争行為等)

	<li>・スパム行為(連続発言等)

	<li>・他プレイヤーのプレイ妨害に繋がる行為(ストーカー等)

	<li>・他プレイヤーの個人情報の無断開示する行為等

	<li>・以下に当てはまる建築物の作成、スキンの使用、発言などの表現行為
		<ul style="margin-left: 1rem;">
			<li>1.わいせつ、又は残虐な物(エロ、グロ系)

			<li>2.反社会的な政治や宗教思想を示唆するもの

		</ul>

	<li>・詐欺行為(なりすまし、取引の詐称等)

	<li>・ルール違反に過度に反応する行為、又は助長する行為

	<li>・当サーバー及び関連サービスに正規以外の方法で接続する、又は接続させる行為

</ul>
<br>
<h3 style="font-size: 1.3rem; margin-left: 1rem">第2項 不正行為</h3>
<ul style="margin-left: 1rem">
	<li>・下記機能のあるMOD、リソースパック、ツール等の使用及び使用を示唆する行為
		<ul style="margin-left: 1rem">
			<li>1.ブロックを透過させる機能のあるもの

			<li>2.鉱石の位置を特定する機能のあるもの

			<li>3.他人から識別不能になる機能のあるもの

			<li>4.自動的に採掘や整地、作物の収穫などを可能にするもの

		</ul>

	<li>・自動連打や自動操作若しくはチート行為を可能にする外部ツールの使用及び使用を示唆する行為

	<li>・ゲーム内不具合を悪用若しくは流布する行為(BUD回路等の一部の不具合を除く)

	<li>・正規ルート以外で入手したMinecraftアカウントを使用して当サーバーに接続する行為

	<li>・Proxy、VPNを経由して当サーバーに接続する行為

</ul>
<br>
<h3 style="font-size: 1.3rem; margin-left: 1rem">第3項 異常負荷をかける行為</h3>
<ul style="margin-left: 1rem">
	<li>・ON,OFFスイッチのない高周波(クロック周期が4RSチック(リピーター最大遅延1つ分))のクロック回路を作成若しくはクロック回路未使用時にON状態のまま放置する行為

	<li>・中規模以上のトラップタワー(基準は<a class="hide-line" href="minecraft.php?category=rules&content=traptower-rules">こちら</a>にて)を公共ポータル等の公共交通機関の16チャンク以内に設置する行為

	<li>・中規模以上の回路(基準は<a class="hide-line" href="minecraft.php?category=rules&content=traptower-rules">こちら</a>にて)を公共ポータル等の公共交通機関の16チャンク以内に設置する行為

	<li>・周辺のALL値が200を超えるような施設の建設(トラップタワーを除く)

	<li>・停止機構の無い村人などの無限増殖装置の作成

</ul>
<br>
<h3 style="font-size: 1.3rem; margin-left: 1rem">第4項 その他</h3>
<ul style="margin-left: 1rem">
	<li>・運営チームからの指示に従わない行為(明らかに不当な指示は除く)

	<li>・運営チームが不適切であると判断した行為

</ul>
<br>
<h2 class="content-subtitle">第2条 サーバーにおける建築と保護のルール</h2>
<h3 style="font-size: 1.3rem; margin-left: 1rem">第1項 保護の設定</h3>
<ul style="margin-left: 1rem">
	<li>・初期スポーンワールド、公共交通機関以外は建築、及びWorldGuardでの保護が可能です。

	<li>(詳しい保護方法は<a class="hide-line" href="minecraft.php?category=plugins&content=worldguard">こちら</a>にて解説します。)

</ul>
<br>
<h3 style="font-size: 1.3rem; margin-left: 1rem">第2項 禁止事項</h3>
<ul style="margin-left: 1rem">
	<li>・公共設備(道路等)を保護範囲に指定すること、及び破壊すること

	<li>・他プレイヤーの建築物を保護範囲に指定すること、及び破壊すること

	<li>・他プレイヤーへの嫌がらせを目的とした保護の作成行為、及び建築、破壊行為

</ul>
<br>
<h3 style="font-size: 1.3rem; margin-left: 1rem">第3項 保護の強制解除、建築物の強制移動</h3>
<mark style="margin-left: 1rem">以下に該当する保護、建築物は移動、解除、撤去する可能性があります。</mark>
<ul style="margin-left: 1rem">
	<li>・本ルールの他の事項に抵触する保護

	<li>・建築途中、又は未建築で30日以上プレイヤーログインが無い保護<br>(完成品か否かは運営が独断で判断します。)

	<li>・公共施設を建築する際の妨げとなる建築物

	<li>・オーナーがBAN処分を受けたプレイヤーのみである保護

	<li>・周辺のALL値が200を超える原因となる保護

	<li>・著しく景観を損なう創作物のある保護

	<li>・公序良俗に反する、若しくは反社会的な建築物

	<li>・無意味、又は不必要な保護であると運営チームが判断した場合

	<li>・他社の著作権、肖像権を侵害する建築物

</ul>
<br>
<h2 class="content-subtitle">第3条 整地のルール</h2>
<h3 style="font-size: 1.3rem; margin-left: 1rem">第1項 整地する際のルール</h3>
<ul style="margin-left: 1rem">
	<li>・整地をする際は下から掘らず、上から掘ってください。<br>(下から掘るとそれを知らずに上から掘ってきた人が落下死する可能性があります。)

	<li>・他人の建築物の下を掘らない

</ul>
<br>
<h2 class="content-subtitle">第4条 処罰の執行及び異議の申し立て</h2>
<h3 style="font-size: 1.3rem; margin-left: 1rem">第1項 処罰の執行</h3>
<ul style="margin-left: 1rem">
	<li>・本ルールに抵触したプレイヤーを発見した場合、度合いに応じて運営が処罰<mark>(警告、無期限BAN)</mark>を実施します。

	<li>・本ルールに抵触しない場合でも運営チームが不適切であると判断した場合は運営チームの合議によって処罰<mark>(警告、無期限BAN)</mark>を実施する可能性があります。

</ul>
<br>
<h3 style="font-size: 1.3rem; margin-left: 1rem">第2項 異議申し立て</h3>
<div style="margin-left: 1rem">
	BAN処分されたプレイヤーは<a class="hide-line" href="minecraft.php?category=others&content=contactus">こちら</a>より運営チームに異議申し立てを行うことができますが、不当な処罰である場合を除き、BANの解除は一切行いません。<br> 又、以下に当てはまる場合は如何なる理由でも異議申し立ては受理されません。
</div>
<ul style="margin-left: 1rem">
	<li>・被処罰者本人以外から投稿された異議申し立て

	<li>・執行から60日以上経過した処罰

</ul>
<br>
<h2 class="content-subtitle">第5条 動画投稿、生放送について</h2>
<h3 style="font-size:1.3rem; margin-left: 1rem">第1項 動画投稿、生放送する際の注意</h3>
<ul style="margin-left: 1rem">
	<li>・動画投稿、生放送をされる際は他プレイヤーの迷惑にならないように注意してください。
	<li>・動画投稿、生放送をする際も本ルールは適用されます。例外は一切ありませんのでご注意下さい。
	<li>・動画、生放送の視聴者をこのサーバーに招待するのはご自由ですが、参加する前に本ルールを読むよう伝えておいてください。
	<li>・動画投稿、生放送をされた際に起こった不具合、損失などのトラブルには運営チームは一切関与しません。当事者で解決するようにしてください。
</ul>
