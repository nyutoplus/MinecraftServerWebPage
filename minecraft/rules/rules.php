<h1 class="content-title">サーバールール</h1>
<p>
	サーバーに参加するプレイヤーの皆様(以下、プレイヤー)は当サーバーに参加した時点で本ルールに同意したものとします。<br>
	本ルールはサーバー内の情勢、運営の都合で予告なく変更されることがあります。変更した際には<a class="hide-line" href="https://discord.gg/wshGjfg">Discordサーバー</a>にて通知しますので是非サーバーに参加してください。<br>
</p>
