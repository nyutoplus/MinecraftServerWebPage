<h2 class="content-subtitle"><strong><a id="4" class="anchor"></a><a href="#4">第4条</a> 処罰の執行及び異議の申し立て</strong></h2>
<h3 style="margin-left: 0.5rem"><strong><a id="4-1" class="anchor"></a><a href="#4-1">第1項</a> 処罰の執行</strong></h3>
<div style="margin-left: 1rem">
	<a id="4-1-1" class="anchor"></a><a href="#4-1-1">1.</a>本ルールに抵触したプレイヤーを発見した場合、度合いに応じて運営が処罰<mark>(警告、無期限BAN)</mark>を実施します。<br>
		処罰内容に関しては<a href="#4-1-3">こちら</a>に基づくものとします。<br>
	<a id="4-1-2" class="anchor"></a><a href="#4-1-2">2.</a>本ルールに抵触しない場合でも運営チームが不適切であると判断した場合は運営チームの合議によって処罰<mark>(警告、無期限BAN)</mark>を実施する可能性があります。<br>
		処罰内容に関しては<a href="#4-1-3">こちら</a>に基づくものとします。<br>
	<a id="4-1-3" class="anchor"></a><a href="#4-1-3">3.</a>処罰執行は各ルールの末尾に記載している点数を元に行います。<br>
		<div style="margin-left:0.5rem">
			<a id="4-1-3-1" class="anchor"></a><a href="#4-1-3-1">1.</a>処罰はルールに抵触するたび点数を加算し、各処罰を実行します。<br>
			<a id="4-1-3-2" class="anchor"></a><a href="#4-1-3-2">2.</a>ルールの点数が10点以下の場合、運営の指示に従えば5点の減点が行われます。<br>
				また、原点が行われた後、再度同様の違反をしたりするなど、反省が見られない場合は減点は無かったものとし、追加で犯したルールの点数が加算されます。<br>
			<a id="4-1-3-3" class="anchor"></a><a href="#4-1-3-3">3.</a>処罰の内容は累計点数を元にして、下記の通り行われます。<br>
			<div style="margin-left: 0.5rem">
				<strong>累計点数が20点未満の場合、警告を行います。<br>
				累計点数が20点を超える場合、1週間の本サーバーへの接続禁止とさせていただきます。<br>
				累計点数が40点を超える場合、警告なしでBANを実行させていただきます。<br>
				<mark>※BANを実行した場合、そのプレイヤーが本サーバーで行った行動すべてがロールバックされます。<br>
				また、その結果は<a href="#4-2">下記</a>の異議申し立てが受理されたとしても覆らないものとします。</mark></strong>
			</div>
		</div>
</div>
<br>
