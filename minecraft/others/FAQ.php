
<h1 class="content-title">よくある質問</h1>
<div style="padding-left: 1rem">
	こちらではよくある質問を掲載しています。<br> 簡単な質問は<a class="hide-line" href="https://discord.gg/kQTpXVR">こちら</a>のDiscordチャンネルか、Minecraft内のチャットで行って下さい。<br> それ以外については<a class="hide-line" href="?category=others&content=contactus">お問い合わせ </a>にて行って下さい。
</div>
<hr>
<div style="padding-left: 1rem">
	<h2 class="question">Q.サーバーへの入り方が分からないです。</h2>
	A.サーバーのIPアドレスなどは<a class="hide-line" href="./">こちら</a>に掲載しています。<br> それでもわからない場合は<a class="hide-line" href="https://discord.gg/CzN2QvG">こちら</a>をご覧ください。
</div>
<hr>
<div style="padding-left: 1rem">
	<h2 class="question">Q.バージョンっていくつ？</h2>
	A. 1.7～1.16.2です。1.16.2以外では不具合がある可能性がございますので不具合がありましたら1.16.2でプレイすることをお勧めします。
</div>
<hr>
<div style="padding-left: 1rem">
	<h2 class="question">Q.どんなコマンドが使えるの？</h2>
	A.導入プラグインで表示されているプラグインのコマンドが使用できます。<br> 一部コマンドは特定の権限が無いと使用できなくなっています。<br> ご了承ください。
</div>
<hr>
<div style="padding-left: 1rem">
	<h2 class="question">Q.サーバーに入れないんだけど？</h2>
	A.クライアントのバージョンを確認してください。<br> また、メンテナンス中はサーバーには入れません。<br> メンテナンス時間については<a class="hide-line" href="?category=status&content=maintenance">こちら</a>にて確認してください。<br>
</div>
<hr>
<div style="padding-left: 1rem">
	<h2 class="question">Q.「あなたのReputation値は、このサーバーの閾値以下です！」と表示されてサーバーに入れない。</h2>
	A.このサーバーではMCBansを導入しています。<br> 閾値は7に設定されており、Reputation値がそれ以下の場合はサーバーに入れません。<br> 自身のReputation値を確認するには<a class="hide-line" href="https://www.mcbans.com/">こちら</a>よりMCBans公式ページに行きプレイヤー名で検索してください。
</div>
<hr>
